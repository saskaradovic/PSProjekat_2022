    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            Customer c = new Customer();
            c.setId(Long.MIN_VALUE);
            c.setFirstName(txtFirstName.getText().trim());
            c.setLastName(txtLastName.getText().trim());
            c.setAddress(txtAddress.getText().trim());
            c.setPhoneNumber(txtPhoneNumber.getText().trim());
            c.setEmail(txtEmail.getText().trim());
            c.setPostalCode(txtPostalCode.getText().trim());

            Controller.getInstance().saveCustomer(c);
            frmMode = FormMode.VIEW;
            setupComponents();
        } catch (Exception ex) {
            ex.getMessage();
    }//GEN-LAST:event_btnSaveActionPerformed
    }
