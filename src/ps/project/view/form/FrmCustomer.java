    private void prepareView() {
        setLocationRelativeTo(null);
        setupComponents();
        fillCbCustomers();

    }

    private void setupComponents() {
        switch (frmMode) {
            case INSERT:
                txtFirstName.setEditable(true);
                txtLastName.setEditable(true);
                txtAddress.setEditable(true);
                txtPhoneNumber.setEditable(true);
                txtEmail.setEditable(true);
                txtPostalCode.setEditable(true);
                btnSave.setEnabled(true);
                btnEnableChanges.setEnabled(true);
                cbCustomers.setEnabled(false);
                break;
            case VIEW:
                txtFirstName.setEditable(false);
                txtLastName.setEditable(false);
                txtAddress.setEditable(false);
                txtPhoneNumber.setEditable(false);
                txtEmail.setEditable(false);
                txtPostalCode.setEditable(false);
                btnSave.setEnabled(false);
                btnEnableChanges.setEnabled(true);
                cbCustomers.setEnabled(false);
                break;

            case EDIT:
                txtFirstName.setEditable(true);
                txtLastName.setEditable(true);
                txtAddress.setEditable(true);
                txtPhoneNumber.setEditable(true);
                txtEmail.setEditable(true);
                txtPostalCode.setEditable(true);
                btnSave.setEnabled(true);
                btnEnableChanges.setEnabled(false);
                cbCustomers.setEnabled(true);
                break;
        }
    }

    private void fillCbCustomers() {
        List<Customer> customers = Controller.getInstance().getAllCustomers();

        for (Customer customer : customers) {
            cbCustomers.addItem(customer);
        }

        cbCustomers.addItemListener(new ItemListener() {
            @Override
            public void itemStateChanged(ItemEvent e) {
                if (e.getStateChange() == ItemEvent.SELECTED) {
                    fillFromCombo();
                }
            }
        });
    }

    private void fillFromCombo() {
        // resetFields();
        Customer selectedCustomer = (Customer) cbCustomers.getSelectedItem();
        System.out.println(selectedCustomer);
        txtFirstName.setText(selectedCustomer.getFirstName());
        txtLastName.setText(selectedCustomer.getLastName());
        txtAddress.setText(selectedCustomer.getAddress());
        txtPhoneNumber.setText(selectedCustomer.getPhoneNumber());
        txtEmail.setText(selectedCustomer.getEmail());
        txtPostalCode.setText(selectedCustomer.getPostalCode());

    }

    private void resetFields() {
        txtFirstName.setText("");
        txtLastName.setText("");
        txtAddress.setText("");
        txtPhoneNumber.setText("");
        txtEmail.setText("");
        txtPostalCode.setText("");
    }
